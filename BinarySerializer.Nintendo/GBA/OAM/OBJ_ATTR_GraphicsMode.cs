namespace BinarySerializer.GBA
{
    public enum OBJ_ATTR_GraphicsMode
    {
        /// <summary>
        /// Normal rendering
        /// </summary>
        REG = 0,

        /// <summary>
        /// Enables alpha blending
        /// </summary>
        BLEND = 1,

        /// <summary>
        /// Object is part of the object window
        /// </summary>
        WIN = 2,
    }
}